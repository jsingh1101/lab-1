#!/usr/bin/env python3

# Create a string object
name = 'Isaac'

# Print the value of the string object
print(name)

# Print the string literal 'name' (this prints the word 'name', not the variable)
print('name')

# Print concatenated string with variable value
print('I have a friend named ' + name)

# Print question with the string value
print('How old are you ' + name + '?')
